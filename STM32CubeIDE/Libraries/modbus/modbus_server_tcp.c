/******************************************************************************
** modbus_tcp_init
*******************************************************************************
* Description    : MODBUS TCP init 
* Input          : *mb: pointer to a modbus structure
* Return         : None 
*******************************************************************************/
void modbus_tcp_init(modbus_t *mb)
{
    rs485_init();
    rs485_clearBufTX();
    rs485_clearBufRX();
    modbus_init(mb, MODBUS_TCP_MODE, (uint8_t*)&rx1_buf[0], (uint8_t*)&tx1_buf[0]);
    modbus_bankreg_init();
}



